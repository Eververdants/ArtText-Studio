// 获取随机诗词
export const getRandomPoem = () => {
    const randomIndex = Math.floor(Math.random() * POEMS.length);
    return POEMS[randomIndex];
};

// 根据语言获取随机诗词
export const getRandomPoemByLang = (lang: 'zh' | 'en') => {
    const filteredPoems = POEMS.filter(poem => poem.lang === lang);
    if (filteredPoems.length === 0) return getRandomPoem();
    const randomIndex = Math.floor(Math.random() * filteredPoems.length);
    return filteredPoems[randomIndex];
};
